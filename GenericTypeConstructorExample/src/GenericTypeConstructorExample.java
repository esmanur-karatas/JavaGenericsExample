
public class GenericTypeConstructorExample {

	public static void main(String[] args) {
		Car a1 = new Car("Toyota", "Corona", 2022);
		Car a2 = new Car("Renault", "Symbol", 2002);
		Car a3 = new Car("BMW", "X1", 2023);
		Car a4 = new Car("Audi", "A5", 2024);
		Car a5 = new Car("Dacia", "Dokker", 2021);
		
	   System.out.println(a1);
	   System.out.println(a2);
	   System.out.println(a3);
	   System.out.println(a4);
	   System.out.println(a5);
	}

}
